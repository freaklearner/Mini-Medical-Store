    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        JOptionPane.showMessageDialog(null,"rows :"+  m.getRowCount());
        if(m.getRowCount()>0)
        {
            int i=0,j=0;
            while(i<m.getRowCount())
            {
                JOptionPane.showMessageDialog(null,"Value : "+m.getValueAt(i, 0));
                if(jComboBox1.getSelectedItem().equals(m.getValueAt(i, 0)))
                {
                    j=1;
                    JOptionPane.showMessageDialog(null,"Inside if");
                    a=a-Float.valueOf(m.getValueAt(i, 3).toString());
                    jTextField2.setText(a+"");
                    m.removeRow(i);
                    break;
                }
                i++;
            }
            if(j==0)
            {
                JOptionPane.showMessageDialog(null,"Invoice doesn't contain "+jComboBox1.getSelectedItem());
            }
           
        }
        jComboBox1.requestFocus(true);
    }//GEN-LAST:event_jButton2ActionPerformed

