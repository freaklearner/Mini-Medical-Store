    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
            try
            {
                if(m.getRowCount()>0)
                {
                    String str="select * from invoice";
                    rs=st.executeQuery(str);
                    rs.last();
                    int x=Integer.valueOf(rs.getString("sno"));
                    String query="insert into invoice(medicine,price,name,dt) values('";
                    int i=0;
                    while(i<m.getRowCount())
                    {
                        query=query+m.getValueAt(i, 0).toString()+",";
                        i++;
                    }
                    query=query.substring(0, query.length()-1);
                    query=query+"',"+a+",'"+jTextField3.getText()+"','"+jLabel5.getText()+"');";
                    st.executeUpdate(query);
                    str="select * from invoice where sno>"+x+";";
                    rs=st.executeQuery(str);
                    rs.next();
                    JOptionPane.showMessageDialog(null,"Bill Generated with No: "+rs.getString("sno"));
                    this.dispose();
                }
            }
            catch(Exception e)
            {
                JOptionPane.showMessageDialog(null,e);

            }
    }//GEN-LAST:event_jButton3ActionPerformed

